// Operadores relacionais
// Igual a (==)
let igual = 5 == 5;
console.log("Igualdade: " + igual); // true

// Identico a (===)
let identico = 5 === '5';
console.log("Igualdade restrita: " + identico); // false

// Diferente de (!=)
let diferente = 5 != 5;
console.log("Diferença: " + diferente); // false

// Não idêntico a (!==)
let diferenteRestrito = 5 !== '5';
console.log("Diferença restrita: " + diferenteRestrito); // true

// Maior que (>)
let maiorQue = 5 > 3;
console.log("Maior que: " + maiorQue); // true

// Menor que (<)
let menorQue = 5 < 3;
console.log("Menor que: " + menorQue); // false

// Maior ou igual a (>=)
let maiorIgual = 5 >= 5;
console.log("Maior ou igual a: " + maiorIgual); // true

// Menor ou igual a (<=)
let menorIgual = 5 <= 3;
console.log("Menor ou igual a: " + menorIgual); // false