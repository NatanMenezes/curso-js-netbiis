// Operadores matemáticos
// Adição
let soma = 5 + 2;
console.log(soma); // 7

// Subtração
let subtracao = 5 - 2;
console.log(subtracao); // 3

// Multiplicação
let multiplicacao = 5 * 2;
console.log(multiplicacao); // 10

// Divisão
let divisao = 5 / 2;
console.log(divisao); // 2.5

// Resto da divisão
let resto = 5 % 2;
console.log(resto); // 1

// Exponenciação
let exponenciacao = 5 ** 2;
console.log(exponenciacao); // 25

// Operadores de incremento e decremento
let numero = 5;

// Incremento e decremento que funcionam com todos os operadores matemáticos
numero = numero + 1;
numero += 3;
numero = numero - 2;
numero -= 2;

// Incremento e decremento que funcionam apenas com soma e subtração
numero++;
numero--;

console.log(numero);

// Operador de concatenação de strings
let nome = 'João';
let complemento = ' e Maria';
let nomeCompleto = nome + complemento;

console.log(nomeCompleto); // João e Maria